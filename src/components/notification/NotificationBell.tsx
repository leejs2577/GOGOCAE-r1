'use client';

import { useState, useEffect } from 'react';
import { Bell, X, Check } from 'lucide-react';
import { Card } from '@/components/ui/card';
import { Button } from '@/components/ui/button';
import { cn } from '@/lib/utils';

interface Notification {
  id: string;
  type: 'request_created' | 'request_assigned' | 'request_updated' | 'request_completed' | 'file_uploaded';
  title: string;
  message: string;
  timestamp: string;
  read: boolean;
  requestId?: string;
}

interface RequestSummary {
  id: string;
  title: string;
  analysis_type: string;
  priority: string;
  status: string;
  created_at: string;
  requester_name?: string;
}

export const NotificationBell = () => {
  const [isOpen, setIsOpen] = useState(false);
  const [notifications, setNotifications] = useState<Notification[]>([]);
  const [requestSummary, setRequestSummary] = useState<RequestSummary[]>([]);
  const [activeTab, setActiveTab] = useState<'notifications' | 'requests'>('notifications');

  // ÏùΩÏßÄ ÏïäÏùÄ ÏïåÎ¶º Í∞úÏàò
  const unreadCount = notifications.filter(n => !n.read).length;

  // Ïã†Í∑ú ÏöîÏ≤≠ Í∞úÏàò (ÏµúÍ∑º 7Ïùº Ïù¥ÎÇ¥)
  const newRequestsCount = requestSummary.filter(r => {
    const createdDate = new Date(r.created_at);
    const sevenDaysAgo = new Date();
    sevenDaysAgo.setDate(sevenDaysAgo.getDate() - 7);
    return createdDate >= sevenDaysAgo && (r.status === 'pending' || r.status === 'assigned');
  }).length;

  // ÏïåÎ¶º ÏùΩÏùå Ï≤òÎ¶¨
  const markAsRead = async (notificationId: string) => {
    try {
      const response = await fetch(`/api/notifications/${notificationId}/read`, {
        method: 'PUT',
      });

      if (response.ok) {
        setNotifications(prev =>
          prev.map(n =>
            n.id === notificationId ? { ...n, read: true } : n
          )
        );
      }
    } catch (error) {
      console.error('Failed to mark notification as read:', error);
    }
  };

  // Î™®Îì† ÏïåÎ¶º ÏùΩÏùå Ï≤òÎ¶¨
  const markAllAsRead = async () => {
    try {
      const response = await fetch('/api/notifications/read-all', {
        method: 'PUT',
      });

      if (response.ok) {
        setNotifications(prev => prev.map(n => ({ ...n, read: true })));
      }
    } catch (error) {
      console.error('Failed to mark all notifications as read:', error);
    }
  };

  // ÏïåÎ¶º Îç∞Ïù¥ÌÑ∞ÏôÄ ÏöîÏ≤≠ ÏöîÏïΩ Í∞ÄÏ†∏Ïò§Í∏∞
  useEffect(() => {
    const fetchData = async () => {
      try {
        // ÏïåÎ¶º Îç∞Ïù¥ÌÑ∞ Í∞ÄÏ†∏Ïò§Í∏∞
        const notificationResponse = await fetch('/api/notifications');
        if (notificationResponse.ok) {
          const notificationData = await notificationResponse.json();
          setNotifications(notificationData.notifications || []);
        }

        // ÏöîÏ≤≠ ÏöîÏïΩ Îç∞Ïù¥ÌÑ∞ Í∞ÄÏ†∏Ïò§Í∏∞
        const requestResponse = await fetch('/api/notifications/requests-summary');
        if (requestResponse.ok) {
          const requestData = await requestResponse.json();
          setRequestSummary(requestData.requests || []);
        }
      } catch (error) {
        console.error('Failed to fetch data:', error);
      }
    };

    fetchData();

    // 30Ï¥àÎßàÎã§ Îç∞Ïù¥ÌÑ∞ Í∞±Ïã†
    const interval = setInterval(fetchData, 30000);
    return () => clearInterval(interval);
  }, []);

  // ÌåùÏóÖÏùÑ Ïó¥ Îïå Î™®Îì† ÏïåÎ¶º ÏùΩÏùå Ï≤òÎ¶¨
  useEffect(() => {
    if (isOpen && unreadCount > 0) {
      // ÏïΩÍ∞ÑÏùò ÏßÄÏó∞ ÌõÑ ÏùΩÏùå Ï≤òÎ¶¨ (ÏÇ¨Ïö©ÏûêÍ∞Ä ÌôïÏù∏Ìï† ÏãúÍ∞Ñ Ï†úÍ≥µ)
      const timer = setTimeout(() => {
        markAllAsRead();
      }, 500);
      return () => clearTimeout(timer);
    }
  }, [isOpen, unreadCount]);

  // ÏïåÎ¶º ÏÇ≠Ï†ú
  const deleteNotification = async (notificationId: string) => {
    try {
      const response = await fetch(`/api/notifications/${notificationId}`, {
        method: 'DELETE',
      });

      if (response.ok) {
        setNotifications(prev => prev.filter(n => n.id !== notificationId));
      }
    } catch (error) {
      console.error('Failed to delete notification:', error);
    }
  };

  // ÏïåÎ¶º ÏïÑÏù¥ÏΩò ÏÉâÏÉÅ
  const getNotificationIcon = (type: Notification['type']) => {
    switch (type) {
      case 'request_created':
        return 'üÜï';
      case 'request_assigned':
        return 'üë§';
      case 'request_updated':
        return 'üîÑ';
      case 'request_completed':
        return '‚úÖ';
      case 'file_uploaded':
        return 'üìé';
      default:
        return 'üìå';
    }
  };

  // ÏãúÍ∞Ñ Ìè¨Îß∑
  const formatTime = (timestamp: string) => {
    const date = new Date(timestamp);
    const now = new Date();
    const diff = Math.floor((now.getTime() - date.getTime()) / 1000); // Ï¥à Îã®ÏúÑ

    if (diff < 60) return 'Î∞©Í∏à Ï†Ñ';
    if (diff < 3600) return `${Math.floor(diff / 60)}Î∂Ñ Ï†Ñ`;
    if (diff < 86400) return `${Math.floor(diff / 3600)}ÏãúÍ∞Ñ Ï†Ñ`;
    if (diff < 604800) return `${Math.floor(diff / 86400)}Ïùº Ï†Ñ`;

    return date.toLocaleDateString('ko-KR', { month: 'short', day: 'numeric' });
  };

  return (
    <div className="relative">
      {/* ÏïåÎ¶º Î≤® Î≤ÑÌäº */}
      <button
        onClick={() => setIsOpen(!isOpen)}
        className="relative p-2 hover:bg-gray-100 rounded-lg transition-colors"
      >
        <Bell className="h-5 w-5 text-gray-600" />
        {unreadCount > 0 && (
          <span className="absolute top-0 right-0 w-5 h-5 bg-red-500 text-white text-xs font-bold rounded-full flex items-center justify-center">
            {unreadCount > 9 ? '9+' : unreadCount}
          </span>
        )}
      </button>

      {/* ÏïåÎ¶º ÎìúÎ°≠Îã§Ïö¥ */}
      {isOpen && (
        <>
          {/* Î∞∞Í≤Ω Ïò§Î≤ÑÎ†àÏù¥ */}
          <div
            className="fixed inset-0 z-40"
            onClick={() => setIsOpen(false)}
          />

          {/* ÏïåÎ¶º Ìå®ÎÑê */}
          <Card className="absolute right-0 top-full mt-2 w-96 max-h-[500px] overflow-hidden z-50 shadow-xl">
            {/* Ìó§Îçî */}
            <div className="p-4 border-b border-gray-200">
              <div className="flex items-center justify-between mb-3">
                <h3 className="font-semibold text-gray-900">ÏïåÎ¶º ÏÑºÌÑ∞</h3>
                <button
                  onClick={() => setIsOpen(false)}
                  className="p-1 hover:bg-gray-100 rounded transition-colors"
                >
                  <X className="h-4 w-4 text-gray-500" />
                </button>
              </div>

              {/* ÌÉ≠ */}
              <div className="flex space-x-1 bg-gray-100 rounded-lg p-1">
                <button
                  onClick={() => setActiveTab('notifications')}
                  className={`flex-1 py-2 px-3 rounded-md text-sm font-medium transition-colors ${
                    activeTab === 'notifications'
                      ? 'bg-white text-gray-900 shadow-sm'
                      : 'text-gray-600 hover:text-gray-900'
                  }`}
                >
                  ÏïåÎ¶º
                  {notifications.filter(n => !n.read).length > 0 && (
                    <span className="ml-1.5 px-1.5 py-0.5 text-xs bg-red-500 text-white rounded-full">
                      {notifications.filter(n => !n.read).length}
                    </span>
                  )}
                </button>
                <button
                  onClick={() => setActiveTab('requests')}
                  className={`flex-1 py-2 px-3 rounded-md text-sm font-medium transition-colors ${
                    activeTab === 'requests'
                      ? 'bg-white text-gray-900 shadow-sm'
                      : 'text-gray-600 hover:text-gray-900'
                  }`}
                >
                  ÏöîÏ≤≠
                  {newRequestsCount > 0 && (
                    <span className="ml-1.5 px-1.5 py-0.5 text-xs bg-blue-500 text-white rounded-full">
                      {newRequestsCount}
                    </span>
                  )}
                </button>
              </div>
            </div>

            {/* ÎÇ¥Ïö© */}
            <div className="max-h-[400px] overflow-y-auto">
              {activeTab === 'notifications' ? (
                // ÏïåÎ¶º ÌÉ≠
                notifications.length === 0 ? (
                  <div className="p-8 text-center text-gray-500">
                    <Bell className="h-12 w-12 mx-auto mb-2 text-gray-300" />
                    <p className="text-sm">ÏÉàÎ°úÏö¥ ÏïåÎ¶ºÏù¥ ÏóÜÏäµÎãàÎã§</p>
                  </div>
                ) : (
                  <div className="divide-y divide-gray-100">
                    {notifications.map((notification) => (
                      <div
                        key={notification.id}
                        className={cn(
                          'p-4 hover:bg-gray-50 transition-colors relative group',
                          !notification.read && 'bg-blue-50/50'
                        )}
                      >
                        <div className="flex items-start space-x-3">
                          {/* ÏïÑÏù¥ÏΩò */}
                          <div className="flex-shrink-0 text-2xl">
                            {getNotificationIcon(notification.type)}
                          </div>

                          {/* ÎÇ¥Ïö© */}
                          <div className="flex-1 min-w-0">
                            <p className="text-sm font-medium text-gray-900 mb-1">
                              {notification.title}
                            </p>
                            <p className="text-xs text-gray-600 mb-2">
                              {notification.message}
                            </p>
                            <p className="text-xs text-gray-400">
                              {formatTime(notification.timestamp)}
                            </p>
                          </div>

                          {/* ÏÇ≠Ï†ú Î≤ÑÌäº */}
                          <button
                            onClick={() => deleteNotification(notification.id)}
                            className="flex-shrink-0 opacity-0 group-hover:opacity-100 transition-opacity p-1 hover:bg-gray-200 rounded"
                          >
                            <X className="h-4 w-4 text-gray-400" />
                          </button>
                        </div>

                        {/* ÏùΩÏßÄ ÏïäÏùå ÌëúÏãú */}
                        {!notification.read && (
                          <div className="absolute left-0 top-1/2 transform -translate-y-1/2 w-1 h-12 bg-blue-500 rounded-r" />
                        )}
                      </div>
                    ))}
                  </div>
                )
              ) : (
                // ÏöîÏ≤≠ ÌÉ≠
                requestSummary.length === 0 ? (
                  <div className="p-8 text-center text-gray-500">
                    <Bell className="h-12 w-12 mx-auto mb-2 text-gray-300" />
                    <p className="text-sm">ÏÉàÎ°úÏö¥ ÏöîÏ≤≠Ïù¥ ÏóÜÏäµÎãàÎã§</p>
                  </div>
                ) : (
                  <div className="divide-y divide-gray-100">
                    {requestSummary.map((request) => (
                      <div
                        key={request.id}
                        className="p-4 hover:bg-gray-50 transition-colors cursor-pointer"
                        onClick={() => {
                          window.location.href = `/requests/${request.id}`;
                        }}
                      >
                        <div className="flex items-start space-x-3">
                          {/* ÏÉÅÌÉú ÏïÑÏù¥ÏΩò */}
                          <div className="flex-shrink-0">
                            <div className={`w-3 h-3 rounded-full mt-1 ${
                              request.status === 'pending' ? 'bg-gray-500' :
                              request.status === 'assigned' ? 'bg-blue-500' :
                              request.status === 'in_progress' ? 'bg-purple-500' :
                              'bg-green-500'
                            }`}></div>
                          </div>

                          {/* ÎÇ¥Ïö© */}
                          <div className="flex-1 min-w-0">
                            <p className="text-sm font-medium text-gray-900 mb-1 line-clamp-1">
                              {request.title}
                            </p>
                            <div className="flex items-center space-x-2 mb-2">
                              <span className="text-xs px-2 py-0.5 bg-blue-100 text-blue-700 rounded">
                                {request.analysis_type}
                              </span>
                              <span className={`text-xs px-2 py-0.5 rounded ${
                                request.priority === 'urgent' ? 'bg-red-100 text-red-700' :
                                request.priority === 'high' ? 'bg-orange-100 text-orange-700' :
                                request.priority === 'medium' ? 'bg-blue-100 text-blue-700' :
                                'bg-gray-100 text-gray-700'
                              }`}>
                                {request.priority === 'urgent' ? 'Í∏¥Í∏â' :
                                 request.priority === 'high' ? 'ÎÜíÏùå' :
                                 request.priority === 'medium' ? 'Î≥¥ÌÜµ' : 'ÎÇÆÏùå'}
                              </span>
                            </div>
                            <p className="text-xs text-gray-400">
                              {formatTime(request.created_at)}
                            </p>
                          </div>
                        </div>
                      </div>
                    ))}
                  </div>
                )
              )}
            </div>
          </Card>
        </>
      )}
    </div>
  );
};
